from PIL import Image, ImageDraw

def create_pixel_art(filename, design_grid, colors, size=(32, 32)):
    # grid is an 8x8 array. We'll scale it to size 32x32 (1 pixel -> 4x4 block)
    grid_w = len(design_grid[0])
    grid_h = len(design_grid)
    img = Image.new('RGBA', (grid_w, grid_h), (0, 0, 0, 0))
    d = ImageDraw.Draw(img)
    
    for y, row in enumerate(design_grid):
        for x, val in enumerate(row):
            if val != 0:
                d.point((x, y), fill=colors[val])
                
    img = img.resize(size, Image.NEAREST)
    img.save(filename)
    print(f"Generated {filename}")

